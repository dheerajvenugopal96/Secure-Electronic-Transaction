package control;

/**
 *
 * @author acer
 */
public class product {
    
    private int id;
    private int merchantid;
    private String imagepath;
    private String category;
    private String brand;
    private String productName;
    private String description;
    private String amount;
    private String deliveryStates;
    private String quantity;
    private String productverified;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the merchantid
     */
    public int getMerchantid() {
        return merchantid;
    }

    /**
     * @param merchantid the merchantid to set
     */
    public void setMerchantid(int merchantid) {
        this.merchantid = merchantid;
    }

    /**
     * @return the imagepath
     */
    public String getImagepath() {
        return imagepath;
    }

    /**
     * @param imagepath the imagepath to set
     */
    public void setImagepath(String imagepath) {
        this.imagepath = imagepath;
    }

    /**
     * @return the category
     */
    public String getCategory() {
        return category;
    }

    /**
     * @param category the category to set
     */
    public void setCategory(String category) {
        this.category = category;
    }

    /**
     * @return the brand
     */
    public String getBrand() {
        return brand;
    }

    /**
     * @param brand the brand to set
     */
    public void setBrand(String brand) {
        this.brand = brand;
    }

    /**
     * @return the productName
     */
    public String getProductName() {
        return productName;
    }

    /**
     * @param productName the productName to set
     */
    public void setProductName(String productName) {
        this.productName = productName;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the amount
     */
    public String getAmount() {
        return amount;
    }

    /**
     * @param amount the amount to set
     */
    public void setAmount(String amount) {
        this.amount = amount;
    }

    /**
     * @return the deliveryStates
     */
    public String getDeliveryStates() {
        return deliveryStates;
    }

    /**
     * @param deliveryStates the deliveryStates to set
     */
    public void setDeliveryStates(String deliveryStates) {
        this.deliveryStates = deliveryStates;
    }

    /**
     * @return the quantity
     */
    public String getQuantity() {
        return quantity;
    }

    /**
     * @param quantity the quantity to set
     */
    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    /**
     * @return the productverified
     */
    public String getProductverified() {
        return productverified;
    }

    /**
     * @param productverified the productverified to set
     */
    public void setProductverified(String productverified) {
        this.productverified = productverified;
    }
    
    
}
